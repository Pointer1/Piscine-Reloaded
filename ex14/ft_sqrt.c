int	ft_sqrt(int nb)
{
	int r;
	int i;
	int s;

	r = 1;
	i = 1;
	s = 1;
	while (s <= nb)
	{
		s = r * r;
		if (s == nb)
			return (i);
		i++;
		r++;
	}
	return (0);
}
